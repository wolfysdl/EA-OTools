Shader::Shader() {}

Shader::Shader(char const *_name, unsigned int _numTechniques, std::vector<VertexDeclElement> const &_declaration, std::vector<Command> _commands, std::vector<GlobalArgument> _globalArguments) {
    name = _name;
    nameLowered = ToLower(name);
    numTechniques = _numTechniques;
    declaration = _declaration;
    commands = _commands;
    globalArguments = _globalArguments;
    vertexSize = 0;
    for (auto const &d : declaration)
        vertexSize += d.Size();
    computationCommandIndex = -1;
    for (unsigned int i = 0; i < _globalArguments.size(); i++) {
        if (_globalArguments[i].type == Shader::ComputationIndex) {
            computationCommandIndex = int(i);
            break;
        }
    }
    // Validate
    if ((_commands.size() % numTechniques) != 0)
        Error("Invalid command buffer in shader " + name);
    else if ((_commands.size() / numTechniques) != (globalArguments.size() + 2))
        Error("Invalid argument buffer in shader " + name);
}

unsigned int Shader::VertexDeclElement::Size() const {
    switch (type) {
    case Float2:
        return 8;
    case Float3:
        return 12;
    case Float4:
        return 16;
    case D3DColor:
    case UByte4:
        return 4;
    }
    return 0;
}

unsigned int Shader::VertexSize() const {
    return vertexSize;
}

int Shader::ComputationCommandIndex() const {
    return computationCommandIndex;
}

Shader::GlobalArgument::GlobalArgument(unsigned int _type, char const *_format) {
    type = _type;
    format = _format;
}

Shader::Command::Command(unsigned char _id, std::vector<int> const &_arguments) {
    id = _id;
    arguments = _arguments;
}

