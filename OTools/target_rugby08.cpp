Shader *TargetRUGBY08::DecideShader(MaterialProperties const &properties) {
    if (!properties.isSkinned) {
        if (properties.isTextured) {
            if (properties.isTransparent)
                return FindShader("Texture2x.Transparent");
            else {
                if (properties.isUnlit)
                    return FindShader("Texture2x");
                else {
                    if (properties.isReflective || properties.isShiny || properties.isMetallic)
                        return FindShader("IrradLitTextureEnvmap");
                    else {
                        if (properties.isLit)
                            return FindShader("IrradLitTexture2x");
                        else
                            return FindShader("Texture2x");
                    }
                }
            }
        }
        else {
            if (properties.isTransparent)
                return FindShader("Gouraud.Transparent");
            else {
                if (properties.isLit && !properties.isUnlit)
                    return FindShader("LitGouraud");
                else
                    return FindShader("Gouraud");
            }
        }
    }
    else {
        if (properties.numUVs > 1) {
            if (properties.isTransparent)
                return FindShader("LitTexture2IrradSkinSubSurfSpec.Transparent");
            else
                return FindShader("LitTexture2IrradSkinSubSurfSpec");
        }
        else
            return FindShader("IrradLitTexture2x_Skin");
    }
}
